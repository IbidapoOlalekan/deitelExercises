package chapterFour;

public class TabularOutput {
    public static void main(String[] args) {
        for (int n = 1; n < 6; n++ ){
            System.out.print(n + " ");
            System.out.print(n * 10 + " ");
            System.out.print(n * 100 + " ");
            System.out.print(n * 1000+ " ");
            System.out.println(" ");


        }
    }
}
